/*!
   Computes the n-d array of shape (1)

   @param[out] a C-array of with 1 elements
*/
void Bicycle::f_c(double m[1]) {
  double * z = new double[4];

  z[0] = sin(x_[1]);
  z[1] = cos(x_[1]);
  z[2] = sqrt(pow(z[1], 2));
  z[3] = z[1]*sin(x_[2]);

  m[0] = front_.R/z[2] - rear_.R/z[2] - front_.c*(z[0]*sin(x_[3]) - z[3]*cos(x_[3])) - rear_.c*z[3] + ls_*z[1]*cos(x_[2]) + front_.r - rear_.r - pow(z[0], 2)*(front_.R - rear_.R)/z[2];

  delete [] z;
}

/*!
   Computes the n-d array of shape (8)

   @param[out] a C-array of with 8 elements
*/
void Bicycle::f_c_dq(double m[8]) {
  double * z = new double[12];

  z[0] = sin(x_[1]);
  z[1] = sin(x_[2]);
  z[2] = cos(x_[1]);
  z[3] = cos(x_[2]);
  z[4] = cos(x_[3]);
  z[5] = -rear_.R;
  z[6] = -rear_.c;
  z[7] = z[0]*z[4];
  z[8] = z[2]*sin(x_[3]);
  z[9] = z[0]/sqrt(pow(z[2], 2));
  z[10] = pow(z[0], 2)/z[2];
  z[11] = z[9]/z[2];

  m[0] = 0;
  m[1] = front_.R*z[11] - front_.c*(z[1]*z[7] + z[8]) - ls_*z[0]*z[3] - z[0]*z[1]*z[6] + z[11]*z[5] - z[2]*z[9]*(front_.R + z[5]) - z[9]*(front_.R*z[10] + front_.R*z[2] + z[10]*z[5] + z[2]*z[5]);
  m[2] = z[2]*(front_.c*z[3]*z[4] - ls_*z[1] + z[3]*z[6]);
  m[3] = -front_.c*(z[1]*z[8] + z[7]);
  m[4] = 0;
  m[5] = 0;
  m[6] = 0;
  m[7] = 0;

  delete [] z;
}

